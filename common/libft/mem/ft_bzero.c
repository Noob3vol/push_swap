#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*s_cpy;

	s_cpy = (unsigned char *)s;
	while (n)
	{
		*s_cpy = 0;
		s_cpy++;
		n--;
	}
}
